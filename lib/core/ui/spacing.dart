  /// Vertical space of 24 logical pixels
  static const verticalSpace24 = SizedBox(height: 24);

  /// Vertical space of 32 logical pixels
  static const verticalSpace32 = SizedBox(height: 32);

  /// Vertical space of 48 logical pixels
  static const verticalSpace48 = SizedBox(height: 48);

  /// Horizontal space of 4 logical pixels
  static const horizontalSpace4 = SizedBox(width: 4);

  /// Horizontal space of 8 logical pixels
  static const horizontalSpace8 = SizedBox(width: 8);

  /// Horizontal space of 12 logical pixels
  static const horizontalSpace12 = SizedBox(width: 12);

  /// Horizontal space of 16 logical pixels
  static const horizontalSpace16 = SizedBox(width: 16);

  /// Horizontal space of 24 logical pixels
  static const horizontalSpace24 = SizedBox(width: 24);

  /// Horizontal space of 32 logical pixels
