/// A utility class that provides commonly used spacing widgets.
class Spacing {
  const Spacing._();

  /// Vertical space of 4 logical pixels
  static const verticalSpace4 = SizedBox(height: 4);

  /// Vertical space of 8 logical pixels
  static const verticalSpace8 = SizedBox(height: 8);

  /// Vertical space of 12 logical pixels
  static const verticalSpace12 = SizedBox(height: 12);

  /// Vertical space of 16 logical pixels
